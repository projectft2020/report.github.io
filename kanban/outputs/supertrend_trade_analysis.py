# 基礎統計分析
def basic_statistics(df):
    """基礎統計分析"""
    print("=" * 60)
    print("📊 Supertrend 交易數據基礎統計")
    print("=" * 60)

    # 解析報酬率（去掉百分號，轉換為浮點數）
