def create_research_manager(task_id: str, workspace_path: str = None) -> ProgressiveResearchManager:
    """
    Create ProgressiveResearchManager for a task

    Args:
        task_id: Task ID
        workspace_path: Workspace base path (default: ~/.openclaw/workspace)

    Returns:
        ProgressiveResearchManager instance
    """
    if workspace_path is None:
        workspace_path = Path.home() / '.openclaw' / 'workspace'
    else:
        workspace_path = Path(workspace_path)

    # Output directory: kanban/projects/{task_id}/research_checkpoints
    output_dir = workspace_path / 'kanban' / 'projects' / task_id / 'research_checkpoints'

    return ProgressiveResearchManager(output_dir=str(output_dir))


