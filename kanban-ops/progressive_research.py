def example_usage():
    """Example usage of ProgressiveResearchManager"""

    print("🔬 Progressive Research Manager - Example Usage\n")

    # Create manager
    import tempfile
    temp_dir = tempfile.mkdtemp()
    mgr = ProgressiveResearchManager(output_dir=temp_dir, checkpoint_interval=3)

    print(f"📁 Output directory: {temp_dir}\n")

    # Simulate research queries
    research_queries = [
        ("HFT microstructure basics", "Results for HFT microstructure..."),
        ("order book dynamics", "Results for order book dynamics..."),
        ("market making strategies", "Results for market making..."),
        ("latency arbitrage", "Results for latency arbitrage..."),
        ("quote stuffing detection", "Results for quote stuffing..."),
        ("tick size impact", "Results for tick size..."),
        ("dark pool impact", "Results for dark pools..."),
        ("HFT regulation 2026", "Results for HFT regulation..."),
        ("quantitative analysis methods", "Results for quant analysis..."),
    ]

    checkpoint_num = 0

    for query, results in research_queries:
        print(f"🔍 Search {mgr.search_count + 1}: {query}")

        need_checkpoint = mgr.record_search(query, results)

        if need_checkpoint:
            checkpoint_num += 1
            print(f"  ⏸️  Checkpoint needed!")

            # Simulate synthesis
            synthesis = f"""
This checkpoint synthesizes findings from searches {checkpoint_num * 3 - 2} to {checkpoint_num * 3}.

Key findings:
- Important insight 1
- Important insight 2
- Important insight 3

Next research directions:
- Direction 1
- Direction 2
"""

            checkpoint_file = mgr.create_checkpoint(synthesis)
            print(f"  ✅ Checkpoint {checkpoint_num} created: {checkpoint_file.name}\n")

    # Generate final report
    print("\n📊 Creating final report...")

    all_checkpoints = mgr.load_checkpoints()
    final_synthesis = f"""
# HFT Microstructure Analysis - Final Report

## Executive Summary
This report synthesizes findings from {len(all_checkpoints)} research checkpoints
covering {mgr.search_count} web searches.

## Key Findings
(Based on all checkpoint syntheses)

## Conclusions
(Final conclusions and recommendations)
"""

    final_file = mgr.create_final_report(final_synthesis)
    print(f"✅ Final report created: {final_file.name}\n")

    # Show progress report
    print(mgr.generate_progress_report())

    # Show summary
    summary = mgr.get_checkpoint_summary()
    print(f"\n📈 Research Statistics:")
    print(f"  • Total Searches: {summary['total_searches']}")
    print(f"  • Total Checkpoints: {summary['total_checkpoints']}")
    print(f"  • Avg Searches/Checkpoint: {summary['total_searches'] / summary['total_checkpoints']:.1f}")


if __name__ == '__main__':
    example_usage()
