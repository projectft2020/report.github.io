    print(f"最大虧損: {df['報酬率_numeric'].min():.2f}%")

    # 報酬率分布
    print("\n📈 報酬率分布:")
    print(f"  > 1.0%: {(df['報酬率_numeric'] > 1.0).sum()} 筆")
